    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {

        $projects = User::join('collaborators', 'users.id', '=', 'collaborators.collab_id')
            ->rightjoin('projects', 'collaborators.project_id', '=', 'projects.id')
            ->where(['collaborators.collab_id' => Auth::user()->id])->orWhere(['projects.user_id' => Auth::user()->id])
            ->select('projects.id','projects.user_id', 'projects.title','collaborators.collab_id')->get();

        return response([
            'status' => 200,
            'projects' => $projects
        ]);
    }

